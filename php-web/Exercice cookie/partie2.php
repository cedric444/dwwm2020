                <div class="titreArticle">2e version</div>
                <div class="texteArticle">Une version alternative venant de George Boucher, qui fut pendant une période
                    le patron de l’auberge Toll House, et de sa fille Carol Cavanagh qui travaillait là vient alors
                    contredire la version de Nestlé, qui affirmait que Wakefield mit les morceaux de chocolat en
                    espérant que ceci fondraient. Carol déclara que le propriétaire, chef accompli et auteur d’un livre
                    de cuisine, en savait assez sur les propriétés du chocolat pour réaliser qu’il ne fonderait pas et
                    se mélangerait à la pâte pendant la cuisson.
                    Boucher affirma que ce furent les vibrations causées par un mixeur électrique de l’époque entrain de
                    mélanger une pâte pour faire des « petits gâteaux » qui firent tomber les barres de chocolat Nestlé
                    (situées sur une étagère au-dessus), en les cassant en petits morceaux et en les mélangeant dans la
                    pâte. Il prétend avoir convaincu Wakefield qui voulait jeter la pâte de la garder afin de ne pas
                    avoir dépensé de l’énergie à la préparer pour rien, conduisant à la découverte de la combinaison
                    dont nous connaissons tous le résultat.</div>
