                <div class="titreArticle">1ère version</div>
                <div class="texteAside">
                    <div class="texteArticle">Wakefield raconta que pendant une préparation de petits gâteaux au
                        chocolat, il lui manquait du chocolat de pâtisserie, et décida donc de le remplacer par des
                        morceaux mi-durs mi-mous de chocolat Nestlé, en pensant qu’ils fondraient et se mélangeraient à
                        la pâte. Il n’en fut rien et ainsi le Cookie était né. Wakefield vendit la recette à Nestlé en
                        échange d’un approvisionnement à vie de chocolat. Chaque sac de morceaux de chocolat Nestlé
                        vendu en Amérique du Nord a une variante de la recette originale imprimée dessus.
                        Durant la 2e Guerre Mondiale, les soldats américains qui étaient présents en Europe partageaient
                        les cookies qu’ils recevaient par paquets. Il fallu peu d’années pour que des centaines écrivent
                        à leur famille pour qu’elle leur envoie ces fameux cookies de Toll House, et Wakefield fut
                        inondée de lettres venant des quatre coins du monde lui demandant sa recette.</div>
