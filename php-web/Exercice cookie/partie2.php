                <div class="titreArticle">Histoire du cookie</div>
                <div class="texteAside">L’histoire du cookie nous vient des Etats-Unis. Il existe deux versions
                    différente de l’invention du cookie, mais nous pouvons être sûr que son nom est d’origine
                    néerlandaise, « koekje » signifiant « petit gâteau », un terme acheminé en Amérique par les colons
                    Hollandais de la nouvelle Amsterdam il y a fort longtemps.
                    Ansi, nous parlerons de cookie pour faire référence au Chocolate Chip Cookie, qui est tout
                    simplement le cookie que nous, non-anglophones connaissons. Car je le rappelle, Cookie signifie en
                    réalité « petit gâteau ».
                    Un autre fait dont nous pouvons être certain est que le cookie a été accidentellement fabriqué par
                    Ruth Graves Wakefield en 1930. Elle tenait une auberge du nom de Toll House dans le Massachusetts.
                    Cette auberge avait une bonne réputation dans les années 1930 pour les produits qu’elle faisait
                    maison.</div>
